package com.myexample.footballdemo.model.dto;

public class PlayerProfileDto {

    private Integer idPlayer;
    private String playerName;
    private Integer age;
    private String position;
    private String clubName;
    private String clubCountry;
    private String clubCompetition;
    private String countryPlayer;
    private Integer countryRank;


    public Integer getIdPlayer() {
        return this.idPlayer;
    }

    public void setIdPlayer(Integer idPlayer) {
        this.idPlayer = idPlayer;
    }

    public String getPlayerName() {
        return this.playerName;
    }

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public Integer getAge() {
        return this.age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getPosition() {
        return this.position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getClubName() {
        return this.clubName;
    }

    public void setClubName(String clubName) {
        this.clubName = clubName;
    }

    public String getClubCountry() {
        return this.clubCountry;
    }

    public void setClubCountry(String clubCountry) {
        this.clubCountry = clubCountry;
    }

    public String getClubCompetition() {
        return this.clubCompetition;
    }

    public void setClubCompetition(String clubCompetition) {
        this.clubCompetition = clubCompetition;
    }

    public String getCountryPlayer() {
        return this.countryPlayer;
    }

    public void setCountryPlayer(String countryPlayer) {
        this.countryPlayer = countryPlayer;
    }

    public Integer getCountryRank() {
        return this.countryRank;
    }

    public void setCountryRank(Integer countryRank) {
        this.countryRank = countryRank;
    }

    
}
